-- 3. 프로시저 생성 및 즉시 실행 로직
DELIMITER $$

DROP PROCEDURE IF EXISTS temp_force_insert$$

CREATE PROCEDURE temp_force_insert()
BEGIN
    DECLARE i INT DEFAULT 1;
    DECLARE j INT;

    -- 영화 ID 1번부터 20번까지 순회
    WHILE i <= 20 DO
            -- 실제로 movie 테이블에 해당 ID가 있는지 확인
            IF (SELECT EXISTS(SELECT 1 FROM movie WHERE movie_id = i)) THEN
                SET j = 1;
                -- 각 영화당 100개씩 삽입 (총 2,000개)
                WHILE j <= 100 DO
                        SET @start = STR_TO_DATE(
                                CONCAT('2026-04-', FLOOR(1 + RAND() * 30), ' ', FLOOR(9 + RAND() * 14), ':',
                                       LPAD(FLOOR(RAND() * 60), 2, '0'), ':00'),
                                '%Y-%m-%d %H:%i:%s'
                                     );

                        INSERT INTO `schedule` (`no`, `movie_id`, `start_at`, `end_at`, `activation`)
                        VALUES (1, i, @start, DATE_ADD(@start, INTERVAL '02:30' HOUR_MINUTE), TRUE);

                        SET j = j + 1;
                    END WHILE;
            END IF;
            SET i = i + 1;
        END WHILE;
END$$

DELIMITER ;

-- 4. 위에서 만든 함수를 바로 실행
CALL temp_force_insert();


-- 5. 깔끔하게 임시 함수 삭제
DROP PROCEDURE IF EXISTS temp_force_insert;
